def subtract_nums(num1, num2):
    """
    Subtract one number from another.

    :param float num1: The minuend (the number to subtract from).
    :param float num2: The subtrahend (the number to subtract).

    :returns: The result of subtracting `num2` from `num1`.
    :rtype: float
    """
    answer = num1 - num2
    return answer
